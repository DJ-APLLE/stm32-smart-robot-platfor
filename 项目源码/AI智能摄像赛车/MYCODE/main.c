/**
  ******************************************************************************
  * @file           : main.c
  * @brief          : AI智能摄像赛车主程序入口
  * @details        : 包含系统初始化、硬件初始化和任务创建
  * @author         : 
  * @date           : 
  ******************************************************************************
  * @attention
  *
  * 此文件是程序的入口点，负责启动整个系统
  * 执行流程:
  *   1. SystemInit() - STM32系统时钟初始化
  *   2. ALL_HARDWARE_INIT() - 初始化所有硬件模块
  *   3. Create_task() - 创建FreeRTOS任务并启动调度器
  *
  ******************************************************************************
  */

#include "includes.h"

/**
  * @brief  主函数 - 程序入口
  * @retval int 返回值（实际不会返回，因为进入FreeRTOS调度器）
  *
  * @details 主函数是程序的入口点，执行以下步骤:
  *          1. 调用SystemInit()初始化STM32系统时钟和核心配置
  *          2. 调用ALL_HARDWARE_INIT()初始化所有硬件模块
  *          3. 调用Create_task()创建FreeRTOS任务并启动调度器
  *          
  * @note   FreeRTOS调度器启动后，主函数不再返回
  *         系统将在任务调度器的管理下运行各个任务
  */
int main(void)
{
	SystemInit();		   // 系统初始化（时钟、向量表等）
	ALL_HARDWARE_INIT();  // 初始化所有硬件模块
	Create_task();       // 创建FreeRTOS任务并启动调度器

}
